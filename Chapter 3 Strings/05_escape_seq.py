a = "Harry is a good boy\nbut not a good student"   #\n is an escape sequence for newline
print(a)

a = "Harry is a good boy but not a good\tstudent" 
print(a) #Harry is a good boy but not a good      student  ..\t is an escape sequence for tab

a = "Harry is a good boy but not a \"good\" student" 
print(a) #Harry is a good boy but not a "good" student .. \" is an escape sequence for double quote
